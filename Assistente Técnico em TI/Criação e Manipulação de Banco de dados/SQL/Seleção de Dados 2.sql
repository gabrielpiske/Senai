#4. Selecione o nome e a localização de todos os departamentos localizados em Chicago.
	SELECT NM_DEPARTAMENTO, LOCALIZACAO 
		FROM DEPARTAMENTO 
        WHERE LOCALIZACAO LIKE "CHICAGO";

#5. Selecione o nome dos empregados que foram contratados após o ano de 1982.
	SELECT NM_EMPREGADO, DATA_CONTRATACAO
		FROM EMPREGADO
		WHERE DATA_CONTRATACAO >"1982-12-30";

#6. Selecione o nome dos departamentos localizados em Nova York.
	SELECT NM_DEPARTAMENTO, LOCALIZACAO
		FROM DEPARTAMENTO
		WHERE LOCALIZACAO LIKE "NEW YORK";

#7. Selecione o nome e a função dos empregados ordenados em ordem alfabética pelo nome.
	SELECT NM_EMPREGADO, FUNCAO
		FROM EMPREGADO
		ORDER BY NM_EMPREGADO, FUNCAO ASC;

#8. Selecione o ID e a função dos empregados que têm salário superior a R$ 2000.
	SELECT ID_EMPREGADO, FUNCAO, SALARIO
		FROM EMPREGADO
        WHERE SALARIO >"2000";

#9. Selecione o nome e o salário de todos os empregados que possuem comissão.
	SELECT NM_EMPREGADO, SALARIO
		FROM EMPREGADO
		WHERE COMISSAO;

#10. Selecione o nome dos empregados cujo nome começa com a letra &quot;A&quot;.
