public class Entrada {

    private String dataped;
    private String dataentr;
    private double total;
    private double frete;
    private int numnf;
    private double imposto;

    public String getDataped() {
        return dataped;
    }

    public void setDataped(String dataped) {
        this.dataped = dataped;
    }

    public String getDataentr() {
        return dataentr;
    }

    public void setDataentr(String dataentr) {
        this.dataentr = dataentr;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }

    public double getFrete() {
        return frete;
    }

    public void setFrete(double frete) {
        this.frete = frete;
    }

    public int getNumnf() {
        return numnf;
    }

    public void setNumnf(int numnf) {
        this.numnf = numnf;
    }

    public double getImposto() {
        return imposto;
    }

    public void setImposto(double imposto) {
        this.imposto = imposto;
    }

}
